const languageToggle = document.querySelector(".language-toggle");
const translatableElements = document.querySelectorAll("[data-i18n]");

function setLanguage(language) {
  const dictionary = translations[language];
  translatableElements.forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) element.textContent = dictionary[key];
  });

  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  document.title = language === "zh" ? "石涛 | 人工智能研究者" : "Tao Shi | AI Researcher";
  document.querySelectorAll("[data-lang-choice]").forEach((choice) => {
    choice.classList.toggle("active", choice.dataset.langChoice === language);
  });
  localStorage.setItem("preferred-language", language);
}

languageToggle.addEventListener("click", () => {
  const currentLanguage = document.documentElement.lang.startsWith("zh") ? "zh" : "en";
  setLanguage(currentLanguage === "en" ? "zh" : "en");
});

const savedLanguage = localStorage.getItem("preferred-language");
if (savedLanguage === "zh") setLanguage("zh");

const menuToggle = document.querySelector(".menu-toggle");
const mobileNav = document.querySelector(".mobile-nav");

function closeMenu() {
  menuToggle.classList.remove("open");
  mobileNav.classList.remove("open");
  menuToggle.setAttribute("aria-expanded", "false");
  menuToggle.setAttribute("aria-label", "Open menu");
}

menuToggle.addEventListener("click", () => {
  const isOpen = mobileNav.classList.toggle("open");
  menuToggle.classList.toggle("open", isOpen);
  menuToggle.setAttribute("aria-expanded", String(isOpen));
  menuToggle.setAttribute("aria-label", isOpen ? "Close menu" : "Open menu");
});

mobileNav.querySelectorAll("a").forEach((link) => link.addEventListener("click", closeMenu));

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.12 },
);

document.querySelectorAll(".reveal").forEach((element) => revealObserver.observe(element));

